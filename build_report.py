def add_para(doc, text: str, style: str) -> None:
    """Добавляет абзац с указанным стилем (передаём имя стиля шаблона)."""
    doc.add_paragraph(text, style=style)


def add_h2(doc, text: str) -> None:
    """Заголовок второго уровня. Стиль heading 2 имеет встроенную автонумерацию
    через w:numPr (выдаёт «1.1, 1.2, …» вне зависимости от наших ручных «2.1»),
    поэтому на уровне абзаца переопределяем numId на 0 — это OOXML-способ
    отказаться от унаследованной из стиля нумерации."""
    p = doc.add_paragraph(text, style=S_HEADING2)
    pPr = p._p.get_or_add_pPr()
    # Удаляем имеющийся numPr (от стиля он всё равно унаследуется)
    existing = pPr.find(qn("w:numPr"))
    if existing is not None:
        pPr.remove(existing)
    # Ставим явный numPr с numId=0 (= no numbering)
    numPr = OxmlElement("w:numPr")
    numId = OxmlElement("w:numId")
    numId.set(qn("w:val"), "0")
    numPr.append(numId)
    pPr.append(numPr)


def add_listing(doc, code: str) -> None:
    """Каждая строка кода — отдельный абзац стиля «Листинг» (Courier New 12).
    Стиль использует contextualSpacing, поэтому строки идут вплотную."""
    for line in code.split("\n"):
        # пустую строку всё равно добавляем — чтобы пустые строки в коде сохранились
        doc.add_paragraph(line, style=S_LISTING)


def add_yellow_placeholder(doc, description: str, ref_kind: str, ref_num: int) -> None:
    """Жёлтый плейсхолдер картинки в стиле «Рисунок» — пользователь заменит
    его в Word на реальное изображение, подпись («Рисунок N – …») остаётся."""
    p = doc.add_paragraph(style=S_FIGURE)
    run = p.add_run(f"[ВСТАВЬТЕ {ref_kind.upper()} {ref_num}: {description}]")
    run.font.highlight_color = WD_COLOR_INDEX.YELLOW


def add_table(doc, headers: list[str], rows: list[list[str]]) -> None:
    """Таблица со стилем Table Grid (id af1)."""
    table = doc.add_table(rows=1 + len(rows), cols=len(headers))
    table.style = doc.styles["Table Grid"]
    # шапка
    for j, h in enumerate(headers):
        cell = table.rows[0].cells[j]
        cell.text = ""
        para = cell.paragraphs[0]
        run = para.add_run(h)
        run.bold = True
        cell.vertical_alignment = WD_ALIGN_VERTICAL.CENTER
    # строки
    for i, row in enumerate(rows, start=1):
        for j, val in enumerate(row):
            cell = table.rows[i].cells[j]
            cell.text = ""
            para = cell.paragraphs[0]
            para.add_run(val)
            cell.vertical_alignment = WD_ALIGN_VERTICAL.CENTER


def render(doc, content: list[tuple], reg: Registry) -> None:
    for item in content:
        kind = item[0]
        if kind == "h1":
            add_para(doc, item[1], S_HEADING1)
        elif kind == "h2":
            add_h2(doc, item[1])
        elif kind == "p":
            text = reg.resolve(item[1])
            # Сахар: абзац, начинающийся с «– », автоматически
            # рендерится как пункт маркированного списка (стиль с
            # одинарным интервалом и автоматическим «-» от Word).
            # Это даёт правильную типографику без ручной конвертации.
            if text.startswith("– "):
                add_para(doc, text[2:], S_BULLET)
            else:
                add_para(doc, text, S_BODY)
        elif kind == "bul":
            add_para(doc, reg.resolve(item[1]), S_BULLET)
        elif kind == "img":
            _, desc, key = item
            _, num = reg.refs[key]
            add_yellow_placeholder(doc, desc, "рисунок", num)
            add_para(doc, f"Рисунок {num} – {desc}", S_FIGURE_CAP)
        elif kind == "list":
            _, desc, key, code = item
            _, num = reg.refs[key]
            add_para(doc, f"Листинг {num} – {desc}", S_ELEM_CAP)
            add_listing(doc, code)
        elif kind == "tbl":
            _, desc, key, headers, rows = item
            _, num = reg.refs[key]
            add_para(doc, f"Таблица {num} – {desc}", S_ELEM_CAP)
            add_table(doc, headers, rows)
        else:
            raise ValueError(f"unknown content kind: {kind}")


