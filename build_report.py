# ОЧИСТКА ОТ ТЕСТОВЫХ АБЗАЦЕВ ШАБЛОНА
# =============================================================================

def strip_test_paragraphs(doc) -> int:
    """Удаляет два «технических» абзаца, оставленных в Test.docx (стили Title/Основной
    с текстом, начинающимся на «ТЕСТ» / «Это тестовый»)."""
    markers = ("ТЕСТ", "Это тестовый")
    target_styles = {S_HEADING1, S_BODY}
    removed = 0
    for paragraph in list(doc.paragraphs):
        style_name = paragraph.style.name if paragraph.style else None
        if style_name in target_styles and paragraph.text.startswith(markers):
            paragraph._element.getparent().remove(paragraph._element)
            removed += 1
    return removed


