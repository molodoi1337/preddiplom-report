# =============================================================================
# ДВУХПРОХОДНАЯ СБОРКА: РЕЕСТР ССЫЛОК → РЕНДЕР
# =============================================================================
#
# Контент описывается списком кортежей. Первый элемент — тип:
#
#   ('h1',    text)
#   ('h2',    text)
#   ('p',     text)               — основной абзац (поддерживает токены {ref:KEY})
#   ('bul',   text)               — пункт маркированного списка
#   ('img',   description, KEY)   — плейсхолдер картинки + подпись «Рисунок N – …»
#   ('list',  description, KEY,
#             code_text)          — листинг кода + подпись «Листинг N – …»
#   ('tbl',   description, KEY,
#             headers, rows)      — таблица + подпись «Таблица N – …»
#
# {ref:KEY} в любом тексте раскрывается в «рисунок 4», «листинг 7», «таблица 1».


@dataclass
class Registry:
    figure: int = 0
    table: int = 0
    listing: int = 0
    refs: dict[str, tuple[str, int]] = field(default_factory=dict)

    def assign(self, content: list[tuple]) -> None:
        for item in content:
            kind = item[0]
            if kind == "img":
                _, _, key = item
                self.figure += 1
                self.refs[key] = ("рисунок", self.figure)
            elif kind == "list":
                _, _, key, _ = item
                self.listing += 1
                self.refs[key] = ("листинг", self.listing)
            elif kind == "tbl":
                _, _, key, _, _ = item
                self.table += 1
                self.refs[key] = ("таблица", self.table)

    def resolve(self, text: str) -> str:
        """Подстановки в тексте:
            {ref:KEY} → 'рисунок 2' / 'таблица 1' / 'листинг 5' (именит. падеж)
            {n:KEY}   → '2' / '1' / '5' (только число — для согласования
                        падежей: 'На рисунке {n:KEY}', 'в таблице {n:KEY}')
        """
        def repl_ref(m: re.Match) -> str:
            key = m.group(1)
            if key not in self.refs:
                return f"<UNKNOWN_REF:{key}>"
            kind, num = self.refs[key]
            return f"{kind} {num}"

        def repl_n(m: re.Match) -> str:
            key = m.group(1)
            if key not in self.refs:
                return f"<UNKNOWN_REF:{key}>"
            return str(self.refs[key][1])

        text = re.sub(r"\{ref:([^}]+)\}", repl_ref, text)
        text = re.sub(r"\{n:([^}]+)\}",   repl_n,   text)
        return text


