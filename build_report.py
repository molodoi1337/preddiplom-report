def fix_cover(doc, cfg: dict) -> int:
    """Находит все runs с жёлтой подсветкой и заменяет текст по содержимому.
    Возвращает количество выполненных замен."""
    # Замены: что_было → что_станет.
    # Обложка имеет ровно 4 жёлтых runs (см. styles.xml): «НАИМЕНОВАНИЕ ОРГАНИЗАЦИИ»,
    # «5» (год в шифре документа), «000» (номер в журнале), «5» (год внизу обложки).
    # «5» встречается дважды — оба раза заменяем на последнюю цифру года.
    plan = {
        "НАИМЕНОВАНИЕ ОРГАНИЗАЦИИ": cfg["org_name_full"],
        "000": cfg["journal_number"],
        "5":   cfg["year_short"][-1],   # последняя цифра «26» → «6»; «27» → «7»
    }
    replaced = 0
    for paragraph in doc.paragraphs:
        for run in paragraph.runs:
            try:
                hl = run.font.highlight_color
            except Exception:
                hl = None
            if hl != WD_COLOR_INDEX.YELLOW:
                continue
            old = run.text
            if old in plan:
                run.text = plan[old]
                # Снимаем подсветку — индикатор «надо поменять» больше не нужен.
                run.font.highlight_color = None
                replaced += 1
    return replaced


