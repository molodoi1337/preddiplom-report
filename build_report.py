def strip_test_paragraphs(doc) -> int:
    """Удаляет тестовые абзацы из шаблона.

    Считаем «тестовыми» абзацы со стилями a7/a3, чьи тексты начинаются с
    «ТЕСТ» либо «Это тестовый». Содержание (стили toc1/toc2) и титульный
    лист не трогаем — у них другие стили или другой текст.
    """
    markers = ("ТЕСТ", "Это тестовый")
    target_names = {STYLE_HEADING, STYLE_BODY}
    removed = 0
    # копия списка: будем удалять прямо в проходе
    for paragraph in list(doc.paragraphs):
        style_name = paragraph.style.name if paragraph.style else None
        if style_name in target_names and paragraph.text.startswith(markers):
            paragraph._element.getparent().remove(paragraph._element)
            removed += 1
    return removed


def append_content(doc) -> None:
    """Дописывает содержимое отчёта.

    Вызовы add_paragraph в python-docx сами вставляют абзац перед
    финальным <w:sectPr>, поэтому колонтитул, размер страницы и поля
    шаблона сохраняются.
    """
    for kind, text in CONTENT:
        style = STYLE_HEADING if kind == "h" else STYLE_BODY
        doc.add_paragraph(text, style=style)


def main() -> None:
    if not TEMPLATE.exists():
        raise SystemExit(f"Не найден шаблон: {TEMPLATE}")

    doc = Document(str(TEMPLATE))

    removed = strip_test_paragraphs(doc)
    append_content(doc)
    doc.save(str(OUTPUT))

    headings = sum(1 for k, _ in CONTENT if k == "h")
    bodies = len(CONTENT) - headings
    print(f"Удалено тестовых абзацев: {removed}")
    print(f"Добавлено заголовков:      {headings}")
    print(f"Добавлено абзацев текста:  {bodies}")
    print(f"Сохранено: {OUTPUT}")
    print("После открытия в Word/LibreOffice обновите Содержание (Ctrl+A → F9).")


if __name__ == "__main__":
    main()
